package Model.Logic.Entity;

public class Orange {
    private int diameter;
    private int vitC;
    private int cost;
    public Orange() {

    }

    public int getDiameter() {
        return diameter;
    }

    public int getVitC() {
        return vitC;
    }

    public int getCost() {
        return cost;
    }

    public void setDiameter(int diameter) {
        this.diameter = diameter;
    }

    public void setVitC(int vitC) {
        this.vitC = vitC;
    }

    public void setCost(int cost) {
        this.cost = cost;
    }
}
